    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $rules = array('name'   => 'required');
        $validator = Validator::make(Input::all(), $rules);
        if($validator->fails()){
            return Redirect::to('admin/categories/create')->withErrors($validator);
        }
        else{
            $category = new Category();
            $category->name     = Input::get('name');
            $category->created_at  = Input::get('created_at');
            $category->updated_at  = Input::get('updated_at');
            
            $category->save();
            Session::flash('message', 'Successfully Created Category');
            return Redirect::to('admin/categories');
        }
    }

    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $show = Category::find($id);
        return View::make('admin.categories.show')->with('show', $show);
    }

